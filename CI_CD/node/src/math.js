
function Soma(a, b) {
	return a + b
}

function Sub(a, b) {
	return a - b
}

function Mult(a, b) {
	return a * b
}

function MultX(a, b) {
	return a * b * a
}

function SomaX(a, b) {
	return a + b + a
}

function SomaXX(a, b) {
	return a + b + a + a
}

function SomaXXX(a, b) {
	return a + b + a + a + a
}

function SomaXXXX(a, b) {
	return a + b + a + a + a + a
}


module.exports = {Soma, SomaX,SomaXX, SomaXXX, SomaXXXX}